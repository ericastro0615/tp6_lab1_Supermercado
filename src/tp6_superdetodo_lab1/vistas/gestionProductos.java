    private void jButton2_nuevoProdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2_nuevoProdActionPerformed
        // TODO add your handling code here:
        try {
            boolean prodYaExistente= false;
            int cod = Integer.parseInt(jTextField1_cod.getText().trim());
            for (Producto aux: Supermercado.listaDeProductos) {
                if(aux.getCodigo() == cod) {
                    JOptionPane.showMessageDialog(this, "error, producto ya cargado");
                    jButton1_eliminarProd.setEnabled(true);
                } else {
                    String nombre= jTextField1_prod.getText().trim();
                    int codigo=  Integer.parseInt(jTextField1_cod.getText());
                    double precio=  Double.parseDouble(jTextField1_precio.getText());
                    int stock=  Integer.parseInt(jTextField1_stock.getText());
                    // = jComboBox1_categoria.getSelectedItem();
                    
                    Supermercado.listaDeProductos.add(new Producto(nombre, codigo, precio, stock, (EnumCategoria) jComboBox1_categoria.getSelectedItem()));
                    
                }
                
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Datos incorrectos");
            limpiarCampos();
    }//GEN-LAST:event_jButton2_nuevoProdActionPerformed
    }
    

    private void limpiarCampos() {
        jTextField1_cod.setText("");
        jTextField1_precio.setText("");
        jTextField1_prod.setText("");
        jTextField1_stock.setText("");
        prodXGestionar = null;
        jButton1_eliminarProd.setEnabled(false);
    }
    
    
