    private void jButton3_guardarProdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3_guardarProdActionPerformed
        // TODO add your handling code here:
        try {
            int cod = Integer.parseInt(jTextField1_cod.getText());
            String nombre = jTextField1_prod.getText();
            int stock = Integer.parseInt(jTextField1_stock.getText());
            int precio = Integer.parseInt(jTextField1_precio.getText());
            EnumCategoria rubro = (EnumCategoria) jComboBox1_categoria.getSelectedItem();
            Producto prodNuevo = new Producto(nombre, cod, precio, stock, rubro);
            if (nombre.isEmpty()) {
                JOptionPane.showMessageDialog(this, "No debe haber campos en blanco");
                return;
            }
            boolean prod = Supermercado.listaDeProductos.add(prodNuevo);
            if (prod) {
                JOptionPane.showMessageDialog(this, "Producto agregado");
            } else {
                JOptionPane.showMessageDialog(this, "Error, código ya existente");
            }
        } catch (NumberFormatException nf) {
            JOptionPane.showMessageDialog(this, "Error, ingresar sólo números enteros en los campos de: código, precio y stock");
        }
        
    }//GEN-LAST:event_jButton3_guardarProdActionPerformed

    private void jButton1_eliminarProdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1_eliminarProdActionPerformed
        // TODO add your handling code here:
        Supermercado.listaDeProductos.remove(prodXGestionar);
        JOptionPane.showMessageDialog(this, "Producto eliminado");
        limpiarCampos();
    }//GEN-LAST:event_jButton1_eliminarProdActionPerformed

    private void jButton1_buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1_buscarActionPerformed
        // TODO add your handling code here:
         try {
            int cod = Integer.parseInt(jTextField1_cod.getText());
            for (Producto aux : Supermercado.listaDeProductos) {
                if (aux.getCodigo() == cod) {
                    prodXGestionar = aux;
                    JOptionPane.showMessageDialog(this, "error, reintente nuevamente");
                    break;
                } else {
                    jTextField1_prod.setText(prodXGestionar.getDescripcion());
                    jTextField1_precio.setText(prodXGestionar.getPrecio() + "");
                    jTextField1_stock.setText(prodXGestionar.getStock() + "");
                    jComboBox1_categoria.setSelectedItem(prodXGestionar.getCategoria());
                    jButton1_eliminarProd.setEnabled(true);
                }
               
            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Error, ingrese un valor númerico para buscar el código");
             limpiarCampos();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error, reintente nuevamente" + e.toString());
            limpiarCampos();
        }
         
    }//GEN-LAST:event_jButton1_buscarActionPerformed

