    private void cabeceraTablaRubro() {
        tablaRubro.addColumn("Nombre");
        tablaRubro.addColumn("CÃ³digo");
        tablaRubro.addColumn("Precio");
        tablaRubro.addColumn("Stock");
        tablaRubro.addColumn("Categoria");
        jTable1_busquedaRubros.setModel(tablaRubro);
    }

    
    private void borrarFilas() {
        int fila = jTable1_busquedaRubros.getRowCount() - 1;
        //se borra de atras hacia adelante 
        for (int f = fila; f >= 0; f--) {
            tablaRubro.removeRow(f);
        }
    }


    private void jButton1_VerTodosRubrosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1_VerTodosRubrosActionPerformed
        // TODO add your handling code here:
        borrarFilas();
        for (Producto aux: Supermercado.listaDeProductos) {
            tablaRubro.addRow(new Object[]{
                    aux.getDescripcion(),
                    aux.getCodigo(),
                    aux.getPrecio(),
                    aux.getStock(),
                    aux.getCategoria()
                    });
        }
    }//GEN-LAST:event_jButton1_VerTodosRubrosActionPerformed

    private void jComboBox1_RubrosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1_RubrosActionPerformed
        // TODO add your handling code here:
        borrarFilas();
        
        EnumCategoria busqCategoria  = (EnumCategoria ) jComboBox1_Rubros.getSelectedItem();
        borrarFilas();
        for (Producto aux : Supermercado.listaDeProductos) {
            if (aux.getCategoria().equals(busqCategoria)) {
                tablaRubro.addRow(new Object[]{
                    aux.getCodigo(),
                    aux.getDescripcion(), 
                    aux.getPrecio(),
                    aux.getStock()
                });
            }
    } 
    }//GEN-LAST:event_jComboBox1_RubrosActionPerformed

    

