    private void intervaloPrecio() {
        //double precioMin = Double.parseDouble(jLabel2_min.getText().trim());
       // double precioMax = Double.parseDouble(jLabel2_max.getText().trim());
        try {
            double precioMin =0 ;
            double precioMax =0;
            
            if (jTextField1_precioMin.getText().trim().equals("") || jTextField1_precioMax.getText().trim().equals("")) {
              precioMin = 0;
               precioMax = 0;
            }else {
                precioMin = Double.parseDouble(jTextField1_precioMin.getText().trim());
                precioMax = Double.parseDouble(jTextField1_precioMax.getText().trim());
            } 
            
            for (Producto aux : Supermercado.listaDeProductos) {
                if ( aux.getPrecio() >= precioMin &&  aux.getPrecio() <= precioMax) {
                    tablaPrecio.addRow(new Object[]{
                        aux.getDescripcion(),
                        aux.getCodigo(),
                        aux.getPrecio(),
                        aux.getStock(),
                        aux.getCategoria() 
                    });
                }
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Ingrese valores numéricos válidos para el rango de precios.");
             
        } catch (Exception exception) {
            JOptionPane.showMessageDialog(this, "Error: " + exception.toString());
        }

    }
