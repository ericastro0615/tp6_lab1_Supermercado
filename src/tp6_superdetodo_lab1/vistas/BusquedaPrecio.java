    private void borrarFilas() {
        int fila = jTable1_busquedaPrecio.getRowCount() - 1;
        //se borra de atras hacia adelante 
        for (int f = fila; f >= 0; f--) {
            tablaPrecio.removeRow(f);
        }
    }


